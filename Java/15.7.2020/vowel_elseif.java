public class vowel_elseif
{
	public static void main(String[] args)
	{
		char ch;
		ch=args[0].charAt(0);
		
		if(ch=='a')
		{
			System.out.println("\n"+ch+" is a Vowel");
		}
		else if(ch=='e')
		{
			System.out.println("\n"+ch+" is a Vowel");
		}
		else if(ch=='i')
		{
			System.out.println("\n"+ch+" is a Vowel");
		}
		else if(ch=='o')
		{
			System.out.println("\n"+ch+" is a Vowel");
		}
		else if(ch=='u')
		{
			System.out.println("\n"+ch+" is a Vowel");
		}
		else
		{
			System.out.println("\n"+ch+" is a Consonnant");
		}
	}
}